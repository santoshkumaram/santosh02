package Operaters;

public class operates01 {
public static void main(String[] args) {
		
		//Arithmetic operates//
		   int x = 5;
		    int y = 3;
		    System.out.println(x + y);//Addition
		    int a = 5;
		    int b = 3;
		    System.out.println(a - b);//Subtraction
		    int c = 5;
		    int d = 3;
		    System.out.println(c * d);//Multiplication
		    int e = 12;
		    int f = 3;
		    System.out.println(e / f);//Division
		    int g = 5;
		    int h = 2;
		    System.out.println(g % h);//Modulus(return the division remainder)
		    int j = 5;
		    ++j;
		    System.out.println(j);//increment(Increases the value of a variable by 1)
		    int k = 5;
		    --k;
		    System.out.println(k);//decrement(Decreases the value of a variable by 1)
		    
		   // Assignment Operators//
		    int i = 5;
		    System.out.println(i);//same as i=5(out put is 5)
		    int l = 5;
		    l += 3;
		    System.out.println(l);//same as l=l+5(out put is 8)
		    int m = 5;
		    m -= 3;
		    System.out.println(m);//same as m=m-3(out put is 2)
		    int n = 5;
		    n *= 3;
		    System.out.println(n);//same as n=n*3(out put is 15)
		    double p = 5;
		    p /= 3;
		    System.out.println(p);// same as p=p/3(out put is 1.66666)
		    int q = 5;
		    q %= 3;
		    System.out.println(q);// same as q=q%3(out put is 2)
		    int a1 = 5;
		    a1 &= 3;
		    System.out.println(a1);// same as a1=a1&3(out put is 1)
		    int b1 = 5;
		    b1 |= 3;
		    System.out.println(b1);// same as b1=b1|3(out put is 7)
		    int c1 = 5;
		    c1 ^= 3;
		    System.out.println(c1);// same as c1=c1^5(out put is 6)
		    int d1 = 5;
		    d1 ^= 3;
		    System.out.println(d1);// same as c1=c1^5(out put is 6)
		    int a2 = 5;
		    a2 >>= 3;
		    System.out.println(a2);// same as a2 = a2 >> 3(out put is 0)
		    int b2 = 5;
		    b2 <<= 3;
		    System.out.println(b2);// same as b2 = b2 << 3(out put is 40)
		    
		    //
		    
		  }
		    

}
